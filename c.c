float sp(unsigned char _, float n[_+2], unsigned char a){
    float r = log2(n[_]) - n[_+1] / n[_];
    n[a] += 1;
    n[_] += 1;
    n[_+1] += n[a] * log2(n[a]) - (n[a]-1) * log2(n[a]-1);
    return r;
}

float sp0(unsigned char _, unsigned len, float n[_+2], unsigned char a[len]){
    float r = 0;
    for(unsigned i=0; i<len; i++)
        r += sp(_, n, a[i]);
    return r / len;
}

float sp1(unsigned char _, unsigned len, float n[_+1][_+2], unsigned char a[len]){
    float r = 0;
    for(unsigned i=0; i<len-1; i++)
        r += fmin(sp(_, n[a[i]], a[i+1]),
                 sp(_, n[_   ], a[i+1]));
    return r / (len-1);
}

